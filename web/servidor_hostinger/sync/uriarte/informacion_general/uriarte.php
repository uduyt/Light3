<h3>FRANCISCO JAVIER DE URIARTE</h3>

<img src="/images/retratouriarte.jpg" style="float:left;" vspace="5" hspace="5"><p style="margin-top:0;"> Francisco Javier de Uriarte y Borja nació en El Puerto de Santa María el 5 de octubre de 1753. Sentó plaza de Guardiamarina en el Departamento de Cádiz en 1774, tomando parte en todas las campañas marítimas de su tiempo: Argel en 1775, Santa Catalina en 1776-77, bloqueo de Gibraltar y combate del cabo Espartel en 1782, al mando del navío "Firme", campaña del Rosellón en 1793, etc. </p>

<p>Participó en la expedición científica al estrecho de Magallanes de 1788-89, a las órdenes de Antonio de Córdoba. En 1794, mandando la fragata Lucía, realizó un viaje al Río de la Plata. Obtuvo sucesivamente el mando de los navíos "Terrible", Concepción", "Principe de Asturias", "Guerrero", "Argonauta" y, finalmente, el "Santísima Trinidad". </p>

<p>En la batalla de Trafalgar, en 1805, mandaba el "Santísima Trinidad", coloso de 130 cañones, el barco más grande de su época. Se batió con el "Victory" de Nelson, y llevando ventaja, acudieron en ayuda de éste el "Temerary" y el "Neptuno". Luchando a la desesperada Uriarte cae herido y es hecho prisionero. El "Santísima Trinidad" se hundió frente a las costas de Barbate. Allí se recuperaron en 1982 dos cañones del barco que fueron colocados delante de la fachada principal del Panteón de Marinos Ilustres (San Fernando, Cádiz). </p>

<p>En 1808, al ser invadida España, presentó la dimisión de su cargo, que el general Mazarredo no aceptó. Invitado a prestar juramento a José Bonaparte, se negó rotundamente, refugiándose en Sevilla y presentándose a la Junta Central, siendo nombrado gobernador militar de la Isla de León, donde dirigió el corte del famoso puente Zuazo en el sitio a que fue sometida la plaza por los franceses. En 1811 obtuvo el mando del arsenal de La Carraca y de allí pasó a Cartagena como gobernador político y militar. Ascendido a teniente general de la Real Armada, fue nombrado capitán general del departamento de Cartagena. </p>

<p>Su desprendimiento era grande, y en varias ocasiones renunció a sus emolumentos en favor de los más necesitados. <p>

<p>En 1822 se retiró a El Puerto de Santa María, donde falleció a los 89 años, el día 29 de noviembre de 1842. Fue enterrado en el cementerio de Santa Cruz y trasladado al Panteón de Marinos Ilustres de San Fernando el 25 de noviembre de 1983. </p>
			
			
			

